def test_group_by_round_captures_bidding_hand_ids(reflector):
    """
    BIDDING events must populate starting_hand_ids[pid] for each player.
    The first BIDDING event seen for a given pid in a round fixes that
    player's starting hand IDs.
    """
    events = [
        {"round": 1, "phase": "BIDDING", "player": 0, "action_id": 3,
         "my_hand": [56, 65, 6]},
        {"round": 1, "phase": "BIDDING", "player": 1, "action_id": 2,
         "my_hand": [62, 32, 4]},
    ]
    rounds = reflector._group_by_round(events)
    assert rounds[1]["starting_hand_ids"][0] == [56, 65, 6]
    assert rounds[1]["starting_hand_ids"][1] == [62, 32, 4]


def test_group_by_round_captures_trick_actions(reflector):
    """
    PLAYING events followed by a trick_end event must produce a tricks
    entry whose 'actions' list contains (player_id, action_id) tuples in
    play order, and whose 'winner' matches the trick_end event.
    """
    events = [
        {"round": 1, "phase": "PLAYING", "player": 0, "action_id": 6,
         "card_text": "[Green 7]",  "my_hand": [6, 65]},
        {"round": 1, "phase": "PLAYING", "player": 1, "action_id": 65,
         "card_text": "[Escape]",   "my_hand": [65]},
        {"round": 1, "phase": "PLAYING", "player": 2, "action_id": 32,
         "card_text": "[Yellow 5]", "my_hand": [32]},
        {"round": 1, "phase": "PLAYING", "player": 3, "action_id": 56,
         "card_text": "[Pirate]",   "my_hand": [56]},
        {"event_type": "trick_end", "round": 1, "winner": 3, "bonus": 0},
    ]
    rounds = reflector._group_by_round(events)
    trick = rounds[1]["tricks"][0]
    assert trick["actions"] == [(0, 6), (1, 65), (2, 32), (3, 56)]
    assert trick["winner"] == 3


def test_group_by_round_starting_hands_text(reflector):
    """
    The first PLAYING event for a player populates starting_hands[pid]
    with translated card text strings (e.g. '[Green 7]').
    """
    events = [
        {"round": 1, "phase": "PLAYING", "player": 0, "action_id": 6,
         "card_text": "[Green 7]", "my_hand": [6]},
        {"event_type": "trick_end", "round": 1, "winner": 0, "bonus": 0},
    ]
    rounds = reflector._group_by_round(events)
    hand_text = rounds[1]["starting_hands"][0]
    # translate_card(6) → "[Green 7]"
    assert any("[Green 7]" in s for s in hand_text)


