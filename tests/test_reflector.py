def test_build_situation_query_contains_round_info(reflector):
    """
    _build_situation_query must embed the round number, the correct round
    stage label, and the player's bid in the returned string.
    Round 7: not > 7, but > 4 → 'Mid Game'.
    """
    r_data = {
        "starting_hands": {0: ["[Green 7]", "[Pirate]"]},
        "starting_hand_ids": {0: [6, 56]},
        "tricks": [],
        "end_event": None,
    }
    bids = [2, 1, 1, 1]
    won  = [3, 1, 0, 0]

    result = reflector._build_situation_query(
        pid=0, round_num=7, r_data=r_data, bids=bids, won=won
    )

    assert "Round 7" in result
    assert "Mid Game" in result
    assert "Bid 2" in result


