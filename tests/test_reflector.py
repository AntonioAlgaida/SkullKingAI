def test_hunger_label_full(reflector):
    """
    delta == 0 (bid met, won == bid) → 'FULL'.
    """
    label = SleepCycleReflector._hunger_label(
        bid=2, won=2, round_num=5, total_won=5
    )
    assert label == "FULL"


def test_hunger_label_overboard(reflector):
    """
    delta < 0 (won > bid, player over-scored) → 'OVERBOARD'.
    """
    label = SleepCycleReflector._hunger_label(
        bid=2, won=3, round_num=5, total_won=5
    )
    assert label == "OVERBOARD"


def test_hunger_label_doomed(reflector):
    """
    When tricks_left == 0 but delta > 0, there is no way to recover → 'DOOMED'.
    bid=2, won=0, round_num=5, total_won=5 → tricks_left = 5-5 = 0, delta = 2.
    """
    label = SleepCycleReflector._hunger_label(
        bid=2, won=0, round_num=5, total_won=5
    )
    assert label == "DOOMED"


def test_hunger_label_starving(reflector):
    """
    When delta == tricks_left (must win every remaining trick) → 'STARVING'.
    bid=2, won=0, round_num=5, total_won=3 → tricks_left=2, delta=2 → STARVING.
    """
    label = SleepCycleReflector._hunger_label(
        bid=2, won=0, round_num=5, total_won=3
    )
    assert label == "STARVING"


def test_hunger_label_hungry(reflector):
    """
    When 0 < delta < tricks_left (still achievable but needs more wins) → 'HUNGRY'.
    bid=3, won=1, round_num=6, total_won=3 → tricks_left=3, delta=2 → HUNGRY.
    """
    label = SleepCycleReflector._hunger_label(
        bid=3, won=1, round_num=6, total_won=3
    )
    assert label == "HUNGRY"


def test_hunger_label_bid_hidden(reflector):
    """
    bid == -1 (bid not yet revealed in BIDDING phase) → 'BID_HIDDEN'.
    """
    label = SleepCycleReflector._hunger_label(
        bid=-1, won=0, round_num=5, total_won=3
    )
    assert label == "BID_HIDDEN"
