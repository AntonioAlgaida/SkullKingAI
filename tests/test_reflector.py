def _trick_plays_for(players_and_cards):
    """
    Build a 'plays' list in the format the reflector expects:
        ["P0 played [Green 7]", "P1 played [Escape]", ...]
    """
    return [f"P{pid} played {card}" for pid, card in players_and_cards]


