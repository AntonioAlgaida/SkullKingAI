def test_find_critical_trick_with_index_over_bid(reflector):
    """
    For an over-bid (won > bid), the critical trick is the (bid+1)th win
    by pid — the one that pushed the player beyond their bid.
    bid=1, won=2 → critical is the 2nd win by P0 (index 2).
    """
    tricks = [
        {"winner": 0, "plays": ["P0 played [Green 5]"]},   # P0's 1st win (bid met)
        {"winner": 1, "plays": ["P1 played [Black 7]"]},   # P0 didn't win
        {"winner": 0, "plays": ["P0 played [Pirate]"]},    # P0's 2nd win → critical
    ]
    trick, idx = reflector._find_critical_trick_with_index(pid=0, bid=1, won=2, tricks=tricks)
    assert idx == 2
    assert trick is tricks[2]


def test_find_critical_trick_with_index_under_bid(reflector):
    """
    For an under-bid (won < bid), the critical trick is the LAST trick
    in which pid did NOT win.
    bid=2, won=1 → last miss is tricks[2] (P0 never won trick 3).
    """
    tricks = [
        {"winner": 0, "plays": ["P0 played [Black 14]"]},  # P0 wins
        {"winner": 1, "plays": ["P1 played [Skull King]"]},# P0 misses
        {"winner": 2, "plays": ["P2 played [Pirate]"]},    # P0 misses — last miss
    ]
    trick, idx = reflector._find_critical_trick_with_index(pid=0, bid=2, won=1, tricks=tricks)
    assert idx == 2
    assert trick is tricks[2]


def test_find_critical_trick_with_index_no_over_win(reflector):
    """
    Under-bid scenario: bid=2, won=1, only one win for P0.
    The last miss must be returned (the latest trick P0 did not win).
    """
    tricks = [
        {"winner": 0, "plays": ["P0 played [Green 14]"]},
        {"winner": 1, "plays": ["P1 played [Pirate]"]},
        {"winner": 1, "plays": ["P1 played [Skull King]"]},
    ]
    trick, idx = reflector._find_critical_trick_with_index(pid=0, bid=2, won=1, tricks=tricks)
    assert trick is not None
    assert idx >= 1
    # Must be the last trick where P0 did not win — tricks[2]
    assert idx == 2


def test_find_critical_trick_exact_bid_returns_none(reflector):
    """
    When won == bid (success), the over-bid branch is not triggered and the
    under-bid branch (else) searches for misses.  If every trick was won by
    pid there are no misses, so the result must be (None, -1).
    bid=2, won=2 → P0 won every trick → no miss → (None, -1).
    """
    tricks = [
        {"winner": 0, "plays": ["P0 played [Black 11]"]},
        {"winner": 0, "plays": ["P0 played [Pirate]"]},
    ]
    trick, idx = reflector._find_critical_trick_with_index(pid=0, bid=2, won=2, tricks=tricks)
    assert trick is None
    assert idx == -1


